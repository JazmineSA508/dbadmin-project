		  <h1>What are Musical Notes?</h1>
		  <p><img src="./images/music_notes.jpg" alt="Rhythm Tree" width="220" height="191"></p>
       	  <p>Music notes are basically the building blocks of music. They tell us not only the pitch that needs to be played/sung at every point of the song, but also how long play those pitches. <strong>There are 7 major notes in music: C, D, E, F, G, A, and B.</strong> After the last note, the scale repeats. Going from one note to the next occurrence of that note (i.e., C to C) is called an octave. The picture shown is a rhythm tree that shows how long to play certain notes. You can see that the closer together the dots are, the less time the notes are played. For every different kind of note, there is a rest that is played (or, rather, not played) for the same amount of time.</p>
