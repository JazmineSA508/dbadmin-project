CREATE TABLE grammy_wins (
  media_id int DEFAULT NULL,
  grammy_category varchar(255) NOT NULL,
  win_artist varchar(100) NOT NULL,
  win_song varchar(100) DEFAULT NULL,
  win_album varchar(100) DEFAULT NULL,
  win_media_type varchar(5) DEFAULT NULL, 
  win_year varchar(4) NOT NULL,
  PRIMARY KEY (win_artist, grammy_category, win_year), 
  CONSTRAINT `grammys_fkey1` FOREIGN KEY (media_id, win_artist, win_song, win_album) REFERENCES music_catalog (id, artist, song_name, album_name) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `grammys_fkey2` FOREIGN KEY (win_media_type) REFERENCES valid_media_type (mtype) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB;

