DROP TABLE IF EXISTS music_catalog;
CREATE TABLE music_catalog (
  artist varchar(100),
  id int(4) UNIQUE,
  media_type varchar(5),
  song_name varchar(100) DEFAULT '',
  collab boolean DEFAULT NULL,
  collab_artists varchar(255) DEFAULT NULL,
  album_name varchar(100) DEFAULT '',
  release_year varchar(4) DEFAULT NULL,
  PRIMARY KEY (id, artist, song_name, album_name),
  CONSTRAINT `mcatalog_fkey1` FOREIGN KEY (artist) REFERENCES artists (name) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `mcatalog_fkey2` FOREIGN KEY (media_type) REFERENCES valid_media_type (mtype) ON DELETE CASCADE ON UPDATE CASCADE
);

